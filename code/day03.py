
"""
数据类型
"""
# 整型 int
print(0b100)    # 二进制整数 => 4
print(0o100)    # 八进制整数 => 64
print(100)      # 十进制整数 => 100
print(0x100)    # 十六进制整数 => 256

# 浮点型 float
print(123.456)      # 数学写法 => 123.456
print(1.23456e2)    # 科学计数法 => 123.456

# 字符串型 str
print("hello") 
print('hello world') 

# 布尔型 bool
print(True)
print(False)

